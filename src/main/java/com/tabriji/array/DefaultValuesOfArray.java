package com.tabriji.array;

import java.util.Arrays;

public class DefaultValuesOfArray {
    public static void main(String[] args) {
        int[] int_array = new int[5];
        System.out.println("int array: " + Arrays.toString(int_array));
        Integer[] integer_array = new Integer[5];
        System.out.println("int array: " + Arrays.toString(integer_array));
        float[] float_array = new float[5];
        System.out.println("int array: " + Arrays.toString(float_array));
    }
}
